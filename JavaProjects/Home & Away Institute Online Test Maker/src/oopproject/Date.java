package oopproject;

public class Date {
	
	private int Month;
	private int Year;
	private int Day;
	
	public Date(){ // Using default constructor to initialize attributes
		Day = 0;
		Month = 0;
		Year = 0;
	}
	
	public int getDay() {
		return Day;
	}

	public void setDay(int day) {
		Day = day;
	}

	public int getMonth() {
		return Month;
	}

	public void setMonth(int month) {
		Month = month;
	}

	public int getYear() {
		return Year;
	}

	public void setYear(int year) {
		Year = year;
	}

	
	
	
	
	
}
